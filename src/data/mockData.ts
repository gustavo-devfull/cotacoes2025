// Função para calcular estatísticas resumidas
export const calculateSummaryStats = (data: CotacaoItem[]) => {
  return {
    totalItems: data.length,
    totalValue: data.reduce((sum, item) => sum + item.amount, 0),
    totalCBM: data.reduce((sum, item) => sum + item.cbm_total, 0)
  };
};